"""Drive2Win benchmark — the unified evaluator.

Run your trained model on a seeded time-trial course. Every iteration's
log reports the same three numbers from this script: completion rate,
median lap time, and mean crashes per run.

Do not edit this file. If everyone uses a different scorer, the leaderboard
is meaningless.

Usage (from the repo root):
    python -m drive2win.benchmark --weights my_nav.npz --runs 5
    python -m drive2win.benchmark --weights my_nav.npz --runs 5 --seed 7

Adapters (for any iteration that uses something other than the default MLP):
    --module drive2win.cnn       # e.g. CNN / hybrid model
    --module drive2win.agent     # e.g. an integrated tournament agent
The named module must expose `make_policy(weights_path) -> Callable`.
"""
from __future__ import annotations
import argparse
import importlib
import json
import time
from pathlib import Path

import numpy as np

from game_client import GameClient
from . import nn as nn_mod
from .eval import run_policy, score_runs
from .normalize import sensors_to_input, clip_action


DEFAULT_SEED = 42
DEFAULT_RUNS = 5
DEFAULT_DURATION = 60.0
TARGET_CHECKPOINTS = 11  # one full lap on the default time-trial course


def make_mlp_policy(weights_path: str):
    """Build a policy function that loads MLP weights from disk."""
    w = nn_mod.load(weights_path)

    def policy(state):
        x = sensors_to_input(state["sensors"])
        return clip_action(nn_mod.forward(x, w))

    return policy


def make_module_policy(module_path: str, weights_path: str):
    """Use a custom module that exposes `make_policy(weights_path) -> Callable`.

    Iterations that use anything other than the default MLP (CNN, hybrid,
    tournament agent) implement this hook so benchmark.py never needs to
    know the internals.
    """
    mod = importlib.import_module(module_path)
    if not hasattr(mod, "make_policy"):
        raise RuntimeError(
            f"{module_path} must define `make_policy(weights_path) -> policy_fn`."
        )
    return mod.make_policy(weights_path)


def run_benchmark(weights: str, runs: int = DEFAULT_RUNS, seed: int = DEFAULT_SEED,
                  duration: float = DEFAULT_DURATION, module: str | None = None,
                  server_url: str = "https://ml.ferit.tech", api_key: str = "None",
                  player_name: str = "benchmark", obstacles: bool = False) -> dict:
    """Run `runs` benchmark laps and return aggregate metrics.

    The seed is sent to the server so terrain/checkpoint layout is
    reproducible across students (so long as the server honors it).
    """
    if module:
        policy = make_module_policy(module, weights)
    else:
        policy = make_mlp_policy(weights)

    client = GameClient(server_url, api_key)
    runs_out = []
    try:
        for i in range(runs):
            session = client.create_session(
                mode="time_trial",
                player_name=f"{player_name}_run{i+1}",
                config={"seed": seed, "wind_enabled": False, "obstacles_enabled": obstacles},
            )
            client.connect_ws()
            time.sleep(0.6)
            print(f"\n  run {i+1}/{runs}  session={session['session_id'][:8]}…")
            result = run_policy(client, policy, duration=duration, hz=20.0)
            print(f"    checkpoints={result['checkpoints_passed']}/{TARGET_CHECKPOINTS}  "
                  f"crashes={result['crashes']}  steps={result['steps']}")
            runs_out.append(result)
            client.disconnect_ws()
            try: client.delete_session()
            except Exception: pass
    finally:
        try: client.disconnect_ws()
        except Exception: pass

    summary = score_runs(runs_out, TARGET_CHECKPOINTS)
    return {"summary": summary, "runs": runs_out, "config": {
        "weights": weights, "module": module, "seed": seed,
        "runs": runs, "duration": duration,
    }}


def main():
    ap = argparse.ArgumentParser(description="Drive2Win benchmark")
    ap.add_argument("--weights", required=True, help="Path to .npz / .pt weights file")
    ap.add_argument("--module", default=None,
                    help="Optional module path with `make_policy(weights_path)`")
    ap.add_argument("--runs", type=int, default=DEFAULT_RUNS)
    ap.add_argument("--seed", type=int, default=DEFAULT_SEED)
    ap.add_argument("--duration", type=float, default=DEFAULT_DURATION)
    ap.add_argument("--server", default="https://ml.ferit.tech")
    ap.add_argument("--api-key", default="None")
    ap.add_argument("--out", default=None, help="Optional JSON output path")
    args = ap.parse_args()

    print("benchmark.py — Drive2Win evaluator")
    print(f"  seed={args.seed}  course=default  20 Hz  {args.duration:.0f} s per run")
    if args.module:
        print(f"  module={args.module}")

    result = run_benchmark(
        weights=args.weights,
        runs=args.runs,
        seed=args.seed,
        duration=args.duration,
        module=args.module,
        server_url=args.server,
        api_key=args.api_key,
    )

    s = result["summary"]
    print("\n" + "=" * 56)
    print(f"  median lap time  : {s['median_lap_time']:.1f} s   (only completed laps)")
    print(f"  completion rate  : {int(s['completion_rate'] * s['n_runs'])}/{s['n_runs']}")
    print(f"  mean crashes     : {s['mean_crashes']:.1f} / run")
    print(f"  best checkpoints : {s['max_checkpoints']}/{TARGET_CHECKPOINTS}")
    print("=" * 56)

    if args.out:
        Path(args.out).write_text(json.dumps(
            {"summary": s, "config": result["config"]}, indent=2))
        print(f"\n  wrote {args.out}")


if __name__ == "__main__":
    main()
